export function setInspectLocale(locale) {
  currentLocale = texts[locale] ? locale : 'zh-CN';
}

export function inspectT(key, params = {}) {
  const trans = texts[currentLocale] || texts['zh-CN'];
  let text = trans[key] || key;
  for (const [name, value] of Object.entries(params)) {
    text = text.replace(`{${name}}`, value);
  }
  return text;
}

export function getSectionHint(title) {
  const key = sectionHintMap[title];
  return key ? inspectT(key) : '';
}

export function getLabelHint(label) {
  const key = labelHintMap[label];
  return key ? inspectT(key) : '';
}
